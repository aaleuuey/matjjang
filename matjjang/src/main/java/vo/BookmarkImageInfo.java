package vo;

public class BookmarkImageInfo {
	private int bfi_idx, bf_idx;
	private String bfi_img, mi_id, si_id;
	
	public int getBfi_idx() {
		return bfi_idx;
	}
	public void setBfi_idx(int bfi_idx) {
		this.bfi_idx = bfi_idx;
	}
	public int getBf_idx() {
		return bf_idx;
	}
	public void setBf_idx(int bf_idx) {
		this.bf_idx = bf_idx;
	}
	public String getBfi_img() {
		return bfi_img;
	}
	public void setBfi_img(String bfi_img) {
		this.bfi_img = bfi_img;
	}
	public String getMi_id() {
		return mi_id;
	}
	public void setMi_id(String mi_id) {
		this.mi_id = mi_id;
	}
	public String getSi_id() {
		return si_id;
	}
	public void setSi_id(String si_id) {
		this.si_id = si_id;
	}
}
