package vo;

public class HeartInfo {
	private String si_id, mi_id;
	private int sh_idx, sh_heart;
	
	public String getSi_id() {
		return si_id;
	}
	public void setSi_id(String si_id) {
		this.si_id = si_id;
	}
	public String getMi_id() {
		return mi_id;
	}
	public void setMi_id(String mi_id) {
		this.mi_id = mi_id;
	}
	public int getSh_heart() {
		return sh_heart;
	}
	public void setSh_heart(int sh_heart) {
		this.sh_heart = sh_heart;
	}
	public int getSh_idx() {
		return sh_idx;
	}
	public void setSh_idx(int sh_idx) {
		this.sh_idx = sh_idx;
	}
	
	
}
